#!/bin/bash

### gamin ###

cd ${SRC}
LOG=gamin-blfs.log

SELF=`basename ${0}`
set_buildenv
set_libdirname
setup_multiarch
if [ ! "${libdirname}" = "lib" ]; then
   extra_conf="--libdir=/usr/${libdirname}"
fi

unpack_tarball gamin-${GAMIN_VER}
cd ${PKGDIR}

max_log_init gamin ${GAMIN_VER} "blfs (shared)" ${CONFLOGS} ${LOG}
CC="${CC-gcc} ${ARCH_CFLAGS}" \
CXX="${CXX-g++} ${ARCH_CFLAGS}" \
CFLAGS="${TGT_CFLAGS}" \
CXXFLAGS="${TGT_CFLAGS}" \
./configure --prefix=/usr --sysconfdir=/etc ${extra_conf} \
   --mandir=/usr/share/man --infodir=/usr/share/info \
   --libexecdir=/usr/${libdirname}/gamin \
   >> ${LOGFILE} 2>&1 &&
echo " o Configure OK" &&
# Reenable this when gamin supports latest inotify
#--enable-inotify \
min_log_init ${BUILDLOGS} &&
make \
   >> ${LOGFILE} 2>&1 &&
echo " o Build OK" &&

min_log_init ${INSTLOGS} &&
make install \
   >> ${LOGFILE} 2>&1 &&
echo " o ALL OK" || barf

